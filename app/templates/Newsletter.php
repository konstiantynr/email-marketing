	var hyperLink = $j('#link').val();
	var field = $j(".edit #listid").val();
	
	$j.ajax({
		  type: 'POST',
		  url: 'templates_ajax.php',
		  data: { templateid: <?php echo $_GET['id']; ?>, field: field, text: hyperLink },
		  beforeSend:function(){
		    // this is where we append a loading image
		   // $('#ajax-panel').html('<div class="loading"><img src="/images/loading.gif" alt="Loading..." /></div>');
		  },
		  success:function(data){ 
			$j('#editModal').removeClass('in');
			setTimeout(function(){
				$j('#editModal').css('display', 'none');
			}, 1000);
			
			$j('.modal-backdrop').removeClass('in');
			$j('.modal-backdrop').remove();
			$j('body').removeClass('modal-open');
	
		  },
		  error:function(){ 
		    // failed request; give feedback to user
		    //$('#ajax-panel').html('<p class="error"><strong>Oops!</strong> Try that again in a few moments.</p>');
		  }
	});
	
	return false;
});
